package pokemon;

public class Pokemon {
    private String nomePokemon;
    private int forcaPokemon;
    private int agilidadePokemon;
    private int inteligenciaPokemon;
    private int vidaPokemon;
    public String getNomePokemon() {
        return nomePokemon;
    }
    public void setNomePokemon(String nomePokemon) {
        this.nomePokemon = nomePokemon;
    }
    public int getForcaPokemon() {
        return forcaPokemon;
    }
    public void setForcaPokemon(int forcaPokemon) {
        this.forcaPokemon = forcaPokemon;
    }
    public int getAgilidadePokemon() {
        return agilidadePokemon;
    }
    public void setAgilidadePokemon(int agilidadePokemon) {
        this.agilidadePokemon = agilidadePokemon;
    }
    public int getInteligenciaPokemon() {
        return inteligenciaPokemon;
    }
    public void setInteligenciaPokemon(int inteligenciaPokemon) {
        this.inteligenciaPokemon = inteligenciaPokemon;
    }
    public int getVidaPokemon() {
        return vidaPokemon;
    }
    public void setVidaPokemon(int vidaPokemon) {
        this.vidaPokemon = vidaPokemon;
    }


}
